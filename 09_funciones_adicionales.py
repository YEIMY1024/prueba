nombre = "YEIMY LOZANO"
print(len(
  nombre))  #len indica que cantidad de letras tiene una cadena de carcteres

print('Uso de los métodos "upper":')
print(
  nombre.upper())  #Convierte todas las letras de los caracteres en mayúscula

print('Uso de los métodos "lower":')
print(
  nombre.lower())  #Convierte todas las letras de los caracteres en minúscula

print('Uso de los métodos "title":')
print(nombre.title())  #Convierte todas las letras de los caracter tipo título

print('Uso de los métodos "count":')
print(nombre.count("E"))  #Cuenta cuantas letras E hay en nombre
